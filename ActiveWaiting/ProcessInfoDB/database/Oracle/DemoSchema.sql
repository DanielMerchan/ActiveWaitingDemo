-- Script for creation of the DEMO Schema --

CREATE TABLESPACE "DEMODAT" LOGGING
DATAFILE 'DEMODAT.dbf' SIZE 512M
EXTENT MANAGEMENT LOCAL SEGMENT SPACE MANAGEMENT AUTO;

CREATE TEMPORARY TABLESPACE "DEMOTMP"
TEMPFILE 'DEMOTMP.dbf' SIZE 256M
AUTOEXTEND ON;

CREATE USER "DEMO" PROFILE "DEFAULT" IDENTIFIED BY "Oracle12c"
DEFAULT TABLESPACE "DEMODAT" TEMPORARY TABLESPACE "DEMOTMP" ACCOUNT UNLOCK;

GRANT "CONNECT" TO "DEMO";
GRANT "RESOURCE" TO "DEMO";
GRANT create session TO "DEMO";
GRANT ALTER ANY INDEX TO "DEMO";
GRANT ALTER ANY SEQUENCE TO "DEMO";
GRANT ALTER ANY TABLE TO "DEMO";
GRANT ALTER ANY TRIGGER TO "DEMO";
GRANT CREATE ANY INDEX TO "DEMO";
